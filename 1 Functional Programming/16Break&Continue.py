# BREAK STATEMENT---
# The break statement terminate the loop containing. It control the program flows to the statement Immediately after the body of loop. If the break statement inside a nested loop, the break statement will terminate inner most loop...

for val in "string":
    if val == 'i': break
    print(val)

# CONTINUE STATEMENT ---
# The continue statement is used to skip the rest of the code inside a loop for current iteration only, Loop does not terminate But continues on with the next iteration. There is no general syntax of continue statement...

for val in "string":
    if val == 'i': continue
    print(val)